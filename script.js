function clearFieldError(field) {
  field.classList.remove("is-invalid");
  field.removeAttribute("aria-invalid");
  field.closest(".field")?.querySelector(".field-error")?.remove();
}

function showFieldError(field, message) {
  clearFieldError(field);
  field.classList.add("is-invalid");
  field.setAttribute("aria-invalid", "true");
  const error = document.createElement("p");
  error.className = "field-error";
  error.textContent = message;
  field.insertAdjacentElement("afterend", error);
}

function validateApplicationForm() {
  const messages = formMessages[currentLanguage];
  const fields = {
    name: applicationForm.elements.name,
    phone: applicationForm.elements.phone,
    loanType: applicationForm.elements["loan-type"],
    amount: applicationForm.elements.amount,
    location: applicationForm.elements.location
  };
  const phoneDigits = fields.phone.value.replace(/\D/g, "");
  const errors = [
    [fields.name, fields.name.value.trim().length >= 2, messages.name],
    [fields.phone, phoneDigits.length >= 9 && phoneDigits.length <= 12, messages.phone],
    [fields.loanType, Boolean(fields.loanType.value), messages.loanType],
    [fields.amount, Number(fields.amount.value) >= 1000, messages.amount],
    [fields.location, fields.location.value.trim().length >= 2, messages.location]
  ];

  errors.forEach(([field]) => clearFieldError(field));
  errors.filter(([, valid]) => !valid).forEach(([field, , message]) => showFieldError(field, message));
  return { valid: errors.every(([, valid]) => valid), firstInvalid: errors.find(([, valid]) => !valid)?.[0] };
}

