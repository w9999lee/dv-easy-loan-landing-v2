applicationForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const status = applicationForm.querySelector(".form-status");
  const validation = validateApplicationForm();

  if (!validation.valid) {
    status.dataset.state = "error";
    status.dataset.message = "invalid";
    status.textContent = formMessages[currentLanguage].invalid;
    validation.firstInvalid.focus();
    return;
  }

  const formData = new FormData(applicationForm);
  const endpoint = applicationForm.dataset.endpoint.trim();

  if (endpoint) {
    try {
      const response = await fetch(endpoint, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(Object.fromEntries(formData.entries()))
      });
      if (!response.ok) throw new Error(`Form endpoint returned ${response.status}`);
      if (typeof window.gtag === "function") {
        window.gtag("event", "form_submit", {
          event_category: "conversion",
          event_label: "short_form"
        });
      }
      status.dataset.state = "success";
      status.dataset.message = "submitted";
      status.textContent = formMessages[currentLanguage].submitted;
    } catch (error) {
      status.dataset.state = "error";
      status.dataset.message = "apiFailure";
      status.textContent = formMessages[currentLanguage].apiFailure;
    }
    return;
  }

  const message = [
    "Hi DV Easy Loan, saya ingin semak kelayakan pinjaman.",
    `Nama: ${formData.get("name")}`,
    `Nombor Telefon: ${formData.get("phone")}`,
    `Jenis Pinjaman: ${formData.get("loan-type")}`,
    `Jumlah pinjaman: RM${formData.get("amount")}`,
    `Lokasi: ${formData.get("location")}`
  ].join("\n");
  const whatsappUrl = `https://wa.me/60194844444?text=${encodeURIComponent(message)}`;

  status.dataset.state = "";
  status.textContent = "";

  try {
    const whatsappWindow = window.open(whatsappUrl, "_blank");
    if (!whatsappWindow) throw new Error("WhatsApp window blocked");
    whatsappWindow.opener = null;
    if (typeof window.gtag === "function") {
      window.gtag("event", "form_submit", {
        event_category: "conversion",
        event_label: "short_form"
      });
    }
    status.dataset.state = "success";
    status.dataset.message = "success";
    status.textContent = formMessages[currentLanguage].success;
  } catch (error) {
    status.dataset.state = "error";
    status.dataset.message = "failure";
    status.textContent = formMessages[currentLanguage].failure;
  }
});

